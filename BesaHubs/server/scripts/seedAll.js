async function seedDatabase() {
  const transaction = await sequelize.transaction();
  
  try {
    console.log('üå± Starting database seed...\n');
    console.log('=' .repeat(60));
    
    await clearDatabase(transaction);
    
    const companies = await seedCompanies(transaction);
    const contacts = await seedContacts(transaction, companies);
    const properties = await seedProperties(transaction, contacts);
    const deals = await seedDeals(transaction, properties, contacts);
    const documents = await seedDocuments(transaction, properties, deals, companies);
    const tasks = await seedTasks(transaction, properties, contacts, deals, companies);
    const leases = await seedLeases(transaction, properties, contacts);
    const debts = await seedDebts(transaction, properties, companies);
    const entities = await seedEntities(transaction, contacts, companies);
    const ownerships = await seedPropertyOwnerships(transaction, properties, entities);
    const relationships = await seedEntityRelationships(transaction, entities);
    const activities = await seedActivities(transaction, properties, contacts, deals, tasks, companies);
    const notifications = await seedNotifications(transaction, properties, contacts, deals, tasks, leases, debts);
    
    await transaction.commit();
    
    console.log('=' .repeat(60));
    console.log('\n‚úÖ Database seeded successfully!\n');
    console.log('üìä Summary:');
    console.log(`   ‚Ä¢ ${companies.length} companies`);
    console.log(`   ‚Ä¢ ${contacts.length} contacts`);
    console.log(`   ‚Ä¢ ${properties.length} properties`);
    console.log(`   ‚Ä¢ ${deals.length} deals`);
    console.log(`   ‚Ä¢ ${documents.length} documents`);
    console.log(`   ‚Ä¢ ${tasks.length} tasks`);
    console.log(`   ‚Ä¢ ${leases.length} leases`);
    console.log(`   ‚Ä¢ ${debts.length} debts`);
    console.log(`   ‚Ä¢ ${entities.length} entities`);
    console.log(`   ‚Ä¢ ${ownerships.length} property ownerships`);
    console.log(`   ‚Ä¢ ${relationships.length} entity relationships`);
    console.log(`   ‚Ä¢ ${activities.length} activities`);
    console.log(`   ‚Ä¢ ${notifications.length} notifications (${notifications.filter(n => !n.isRead).length} unread)`);
    console.log('\nüéâ All done!\n');
    
    process.exit(0);
  } catch (error) {
    await transaction.rollback();
    console.error('\n‚ùå Error seeding database:', error);
    console.error('\nStack trace:', error.stack);
    process.exit(1);
  }
}

seedDatabase();
